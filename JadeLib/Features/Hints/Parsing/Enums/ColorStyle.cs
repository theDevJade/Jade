namespace RueI.Parsing.Enums;

/// <summary>
/// Represents the style used for a color parameter.
/// </summary>
public enum ColorStyle
{
    /// <summary>
    /// Indicates that the color parameter is a named color.
    /// </summary>
    Named,

    /// <summary>
    /// Indicates that the color parameter is a hex code.
    /// </summary>
    HexCode,
}
